# van ... tot net niet het ander getal
# range(7,7): laatste getal wint -> print niks

for i in range(10):
    print(i)

print(' ')

for i in range(0,10):
    print(i)

print(' ')

for i in range(2,7):
    print(i)

print(' ')

for i in range(-6, 7, 2):
    print(i)

print(' ')

for i in range(6, -6, -2):
    print(i)

print(' ')

